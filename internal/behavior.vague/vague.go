// New returns new *Behavior with Vague action.
func New(name string) *behavior.Behavior {
	min := config.VagueMinInterval
	max := config.VagueMaxInterval
	delay := config.VagueBootupDelay

	b := behavior.New(name, min, max, action)
	b.SetBootupDelay(delay)

	return b
}

// action picks a vague phrase at random and tweets it.
